def commands(cmd):
    import numpy as np
    iters = np.arange(0.01, 0.6, 0.01)
    j = 0
    while(cmd.running):
        pulse1 = iters[j]
        cmd.set_var('pulse', pulse1)
        print('\n-------o-------')
        print('Run #%d/%d, with variables:\npulse = %g\n'%(j+1, len(iters), pulse1))
        cmd.run(wait_end=True, add_time=1000)
        j += 1
        if j == len(iters):
            cmd.stop()
    return cmd
