def commands(cmd):
    import numpy as np
    iters = np.arange(0.04, 0.1, 0.01)
    j = 0
    while(cmd.running):
        t11 = iters[j]
        cmd.set_var('t1', t11)
        print('\n-------o-------')
        print('Run #%d/%d, with variables:\nt1 = %g\n'%(j+1, len(iters), t11))
        cmd.run(wait_end=True, add_time=100)
        j += 1
        if j == len(iters):
            cmd.stop()
    return cmd
