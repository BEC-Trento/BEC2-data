def commands(cmd):
    import numpy as np
    x_arr, tpulse_arr = np.mgrid[0:1:1, 0.1:2.5:0.1, ]
    iters = list(zip(x_arr.ravel(), tpulse_arr.ravel()))
    j = 0
    while(cmd.running):
        x1, tpulse1 = iters[j]
        cmd.set_var('x', x1)
        cmd.set_var('tpulse', tpulse1)
        print('\n-------o-------')
        print('Run #%d/%d, with variables:\nx = %g\ntpulse = %g\n'%(j+1, len(iters), x1, tpulse1))
        cmd.run(wait_end=True, add_time=1000)
        j += 1
        if j == len(iters):
            cmd.stop()
    return cmd
